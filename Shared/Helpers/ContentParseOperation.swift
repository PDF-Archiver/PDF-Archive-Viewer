import Foundation

class ContentParseOperation: Operation {

    let document: Document
    private let options: ParsingOptions

    init(with document: Document, options: ParsingOptions) {
        self.document = document
        self.options = options
    }

    override func main() {

        if isCancelled {
            return
        }
        document.parseContent(options)
    }
}
